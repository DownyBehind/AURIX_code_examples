/* Define LEDs port pins */
#define LED1                            &MODULE_P13,0
#define LED2                            &MODULE_P13,1
#define LED3                            &MODULE_P13,2

/* The minimum FSP fault state time in seconds after an alarm is triggered */
#define MIN_FAULT_STATE_TIME            2U

/* Delay in ms before triggering the SW ALarm */
#define DELAY_BEFORE_ALARM              500U

/* Fault state time in ms before calling the FSP release command */
#define DELAY_BEFORE_FSP_RELEASE_CMD    500U

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
void wait_ms(uint32 ms);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Initialize the FSP protocol */
void init_SMU_FSP(void)
{
    /* Configure and initialize the FSP */
    IfxSmu_configFsp configFsp;
    IfxSmu_initFspConfig(&configFsp);

    /* Enable transition from fault to run state */
    configFsp.enableFaultToRunStateTransition = TRUE;
    /* Set the minimum fault state time to 2 seconds */
    configFsp.faultStateTime = MIN_FAULT_STATE_TIME;

    /* Initialize the FSP */
    IfxSmu_initFsp(&MODULE_SMU, &configFsp);

    /* Enter the fault free state "Run" (after the Power-on Reset (PORST) the FSP is in fault state) */
    IfxSmu_releaseFsp(&MODULE_SMU);

    /* Enable the SMU configuration */
    IfxSmu_unlock(&MODULE_SMU);

    /* Enable Software Alarm 0 to trigger FSP fault state */
    IfxSmu_enableAlarmFsp(&MODULE_SMU, IfxSmu_Alarm_SoftwareAlarm0, TRUE);

    /* After configuration, unlock of the SMU configuration */
    IfxSmu_lock(&MODULE_SMU);

    /* Start the SMU state machine (SSM) */
    IfxSmu_start(&MODULE_SMU);

    /* Configure LED1, LED2 and LED3 port pins */
    IfxPort_setPinMode(LED1, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(LED2, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(LED3, IfxPort_Mode_outputPushPullGeneral);

    /* Turn off LEDs (LEDs are low-level active) */
    IfxPort_setPinState(LED1, IfxPort_State_high);
    IfxPort_setPinState(LED2, IfxPort_State_high);
    IfxPort_setPinState(LED3, IfxPort_State_high);
}

/* Trigger an Alarm and execute the FSP protocol */
void run_FSP_Protocol(void)
{
    /* Delay 500 ms before triggering the Alarm */
    wait_ms(DELAY_BEFORE_ALARM);

    /* Trigger Software Alarm 0 */
    if(IfxSmu_triggerAlarm(&MODULE_SMU, IfxSmu_Alarm_SoftwareAlarm0) == 1)
    {
        /* Turn on LED1 to indicate the alarm is triggered */
        IfxPort_setPinState(LED1, IfxPort_State_low);
    }

    /* Wait 500 ms in fault state */
    wait_ms(DELAY_BEFORE_FSP_RELEASE_CMD);

    /* Enable the alarm clearing. This has to be done before clearing the status flag */
    if(IfxSmu_enableClearAlarmStatus(&MODULE_SMU) == 1)
    {
        /* Clear the alarm status flag */
        IfxSmu_clearAlarm(&MODULE_SMU, IfxSmu_Alarm_SoftwareAlarm0);

        /* Call releaseFSP command to switch to fault free state "run" */
        if(IfxSmu_releaseFsp(&MODULE_SMU) == 1)
        {
            /* Turn on LED2 to indicate that release FSP command is sent */
            IfxPort_setPinState(LED2, IfxPort_State_low);
        }
    }

    /* Wait while FSP state is still in fault state */
    while((MODULE_SMU.STS.B.FSP & 0x1) == 0x0);

    /* Turn on LED3 to indicate that release FSP is switched to fault free state */
    IfxPort_setPinState(LED3, IfxPort_State_low);
}

/* Waits until a timeout in milliseconds */
void wait_ms(uint32 ms)
{
    sint32 Fsys = IfxStm_getFrequency(&MODULE_STM0);
    Ifx_TickTime waitms = (Fsys / (1000 / ms));

    wait(waitms);
}
