/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
/* Macro to define Interrupt Service Routine. */
IFX_INTERRUPT(GPT12_T4_Int0_Handler, 0, ISR_PRIORITY_GPT12_T4_INT);

/* Interrupt Service Routine of timer T4, gets triggered after timer overflow */
void GPT12_T4_Int0_Handler(void)
{
    IfxCpu_enableInterrupts();      /* Allows the interruptions of this function by higher prioritized interrupts   */

    /* Display a message when the interrupt occurs */
    IfxStdIf_DPipe_print(&g_stdInterface, "T4 Interrupt (priority %d) started\n\r", ISR_PRIORITY_GPT12_T4_INT);

    /* Wait 600ms
     * This will not be the case since this ISR will be interrupted by two higher priority timers (T3 and T2)
     */
    wait_ms(DISPLAY_TIME);

    /* Display a message when the interrupt finishes */
    IfxStdIf_DPipe_print(&g_stdInterface, "T4 Interrupt (priority %d) ended\n\r", ISR_PRIORITY_GPT12_T4_INT);
}

/* Macro to define Interrupt Service Routine. */
IFX_INTERRUPT(GPT12_T3_Int0_Handler, 0, ISR_PRIORITY_GPT12_T3_INT);

/* Interrupt Service Routine of timer T3, gets triggered after timer overflow */
void GPT12_T3_Int0_Handler(void)
{
    IfxCpu_enableInterrupts();      /* Allows the interruption of this function by higher prioritized interrupts    */

    /* Display a message when the interrupt occurs */
    IfxStdIf_DPipe_print(&g_stdInterface, "T3 Interrupt (priority %d) started\n\r", ISR_PRIORITY_GPT12_T3_INT);

    /* Wait 600ms */
    wait_ms(DISPLAY_TIME);

    /* Display a message when the interrupt finishes */
    IfxStdIf_DPipe_print(&g_stdInterface, "T3 Interrupt (priority %d) ended\n\r", ISR_PRIORITY_GPT12_T3_INT);
}

/* Macro to define Interrupt Service Routine. */
IFX_INTERRUPT(GPT12_T2_Int0_Handler, 0, ISR_PRIORITY_GPT12_T2_INT);

/* Interrupt Service Routine of timer T2, gets triggered after timer overflow */
void GPT12_T2_Int0_Handler(void)
{
    IfxCpu_enableInterrupts();      /* Allows the interruptions of this function by higher prioritized interrupts   */

    /* Display a message when the interrupt occurs */
    IfxStdIf_DPipe_print(&g_stdInterface, "T2 Interrupt (priority %d) started\n\r", ISR_PRIORITY_GPT12_T2_INT);

    /* Wait 600ms */
    wait_ms(DISPLAY_TIME);

    /* Display a message when the interrupt finishes */
    IfxStdIf_DPipe_print(&g_stdInterface, "T2 Interrupt (priority %d) ended\n\r", ISR_PRIORITY_GPT12_T2_INT);
}

/* This function initializes timers T2, T3 and T4 of the block GPT1 of module GPT12 */
void init_GPT12_module(void)
{
    /* Enable GPT12 module */
    IfxGpt12_enableModule(&MODULE_GPT120);
    /* Select 32 as prescaler for the GPT1 module -> slowest clock frequency */
    IfxGpt12_setGpt1BlockPrescaler(&MODULE_GPT120, IfxGpt12_Gpt1BlockPrescaler_32);

    /* Set timers T2, T3 and T4 in timer mode */
    IfxGpt12_T2_setMode(&MODULE_GPT120, IfxGpt12_Mode_timer);
    IfxGpt12_T3_setMode(&MODULE_GPT120, IfxGpt12_Mode_timer);
    IfxGpt12_T4_setMode(&MODULE_GPT120, IfxGpt12_Mode_timer);
    /* Select 128 as prescaler for T2, T3, T4 to slow down the clock of GPT1 */
    IfxGpt12_T2_setTimerPrescaler(&MODULE_GPT120, IfxGpt12_TimerInputPrescaler_128);
    IfxGpt12_T3_setTimerPrescaler(&MODULE_GPT120, IfxGpt12_TimerInputPrescaler_128);
    IfxGpt12_T4_setTimerPrescaler(&MODULE_GPT120, IfxGpt12_TimerInputPrescaler_128);

    /* Service request configuration */
    /* Get source pointer of timer T2, initialize and enable */
    volatile Ifx_SRC_SRCR *src = IfxGpt12_T2_getSrc(&MODULE_GPT120);
    IfxSrc_init(src, IfxSrc_Tos_cpu0, ISR_PRIORITY_GPT12_T2_INT);                       /* Highest priority         */
    IfxSrc_enable(src);
    /* Get source pointer of timer T3, initialize and enable */
    src = IfxGpt12_T3_getSrc(&MODULE_GPT120);
    IfxSrc_init(src, IfxSrc_Tos_cpu0, ISR_PRIORITY_GPT12_T3_INT);                       /* Intermediate priority    */
    IfxSrc_enable(src);
    /* Get source pointer of timer T4, initialize and enable */
    src = IfxGpt12_T4_getSrc(&MODULE_GPT120);
    IfxSrc_init(src, IfxSrc_Tos_cpu0, ISR_PRIORITY_GPT12_T4_INT);                       /* Lowest priority          */
    IfxSrc_enable(src);

    /* Start timer T4 */
    IfxGpt12_T4_run(&MODULE_GPT120, IfxGpt12_TimerRun_start);
    /* Add a delay to ensure that lowest priority ISR is executed first */
    wait_ms(100);

    /* Start timers T2 & T3 */
    IfxGpt12_T2_run(&MODULE_GPT120, IfxGpt12_TimerRun_start);
    /* Add a delay to ensure it does not start before T2  */
    wait_ms(5);
    IfxGpt12_T3_run(&MODULE_GPT120, IfxGpt12_TimerRun_start);

}

/* Waits until a timeout in milliseconds */
void wait_ms(uint32 ms)
{
    sint32 Fsys = IfxStm_getFrequency(&MODULE_STM0);
    Ifx_TickTime waitms = (Fsys / (1000 / ms));

    wait(waitms);
}

/* ASCLIN TX Interrupt Service Routine */
IFX_INTERRUPT(asclin_Tx_ISR, 0, ISR_PRIORITY_ASCLIN_TX);

void asclin_Tx_ISR(void)
{
    IfxStdIf_DPipe_onTransmit(&g_stdInterface);
}

/* ASCLIN RX Interrupt Service Routine */
IFX_INTERRUPT(asclin_Rx_ISR, 0, ISR_PRIORITY_ASCLIN_RX);

void asclin_Rx_ISR(void)
{
    IfxStdIf_DPipe_onReceive(&g_stdInterface);
}

/* ASCLIN Error Interrupt Service Routine */
IFX_INTERRUPT(asclin_Er_ISR, 0, ISR_PRIORITY_ASCLIN_ER);

void asclin_Er_ISR(void)
{
    IfxStdIf_DPipe_onError(&g_stdInterface);
}

/* Function to initialize the ASCLIN module */
void init_UART(void)
{
    IfxAsclin_Asc_Config ascConf;

    IfxAsclin_Asc_initModuleConfig(&ascConf, &MODULE_ASCLIN0);

    /* Set the desired baud rate */
    ascConf.baudrate.prescaler = ASC_PRESCALER;
    ascConf.baudrate.baudrate = ASC_BAUDRATE;
    ascConf.baudrate.oversampling = IfxAsclin_OversamplingFactor_16;            /* Set the oversampling factor      */

    /* Configure the sampling mode */
    ascConf.bitTiming.medianFilter = IfxAsclin_SamplesPerBit_three;             /* Set the number of samples per bit*/
    ascConf.bitTiming.samplePointPosition = IfxAsclin_SamplePointPosition_8;    /* Set the first sample position    */

    /* ISR priorities and interrupt target */
    ascConf.interrupt.txPriority = ISR_PRIORITY_ASCLIN_TX;
    ascConf.interrupt.rxPriority = ISR_PRIORITY_ASCLIN_RX;
    ascConf.interrupt.erPriority = ISR_PRIORITY_ASCLIN_ER;
    ascConf.interrupt.typeOfService = IfxSrc_Tos_cpu0;

    /* FIFO configuration */
    ascConf.txBuffer = g_AscTxBuffer;
    ascConf.txBufferSize = ASC_TX_BUFFER_SIZE;
    ascConf.rxBuffer = g_AscRxBuffer;
    ascConf.rxBufferSize = ASC_RX_BUFFER_SIZE;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins = {
            NULL, IfxPort_InputMode_pullUp,                                     /* CTS port pin not used            */
            &IfxAsclin0_RXA_P14_1_IN, IfxPort_InputMode_pullUp,                 /* RX port pin                      */
            NULL, IfxPort_OutputMode_pushPull,                                  /* RTS port pin not used            */
            &IfxAsclin0_TX_P14_0_OUT, IfxPort_OutputMode_pushPull,              /* TX port pin                      */
            IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConf.pins = &pins;

    /* Initialize the module */
    IfxAsclin_Asc_initModule(&g_asc, &ascConf);

    /* Initialize the Standard Interface */
    IfxAsclin_Asc_stdIfDPipeInit(&g_stdInterface, &g_asc);
}
