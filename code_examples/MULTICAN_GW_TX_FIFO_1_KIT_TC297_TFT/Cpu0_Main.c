 /*\title MULTICAN in GATEWAY mode using TX FIFO
 * \abstract MULTICAN in Gateway mode is used to exchange data using a gateway with a TX FIFO structure between multiple nodes, implemented in the same device using Loop-Back mode.
 * \description The CAN messages are sent from CAN node 2 over CAN bus (in case of Loop-Back
 *              mode all nodes can access the internal bus). CAN node 0 receives the transmitted
 *              messages but also immediately gateways the received data to CAN node 1. Three
 *              message objects allocated to CAN node 1 define a TX FIFO buffer structure.
 *              Immediately, upon the reception of the data via gateway, CAN node 1 transmits
 *              the received data. The data transmitted by the CAN node 1 is received by the CAN
 *              node 3. The content of the received data will be compared to the content of the
 *              transmitted CAN messages together with the FIFO status check and in case of success,
 *              a LED is turned on to confirm successful message reception.
 *
 * \name MULTICAN_GW_TX_FIFO_1_KIT_TC297_TFT
 * \version V1.0.2
 * \board APPLICATION KIT TC2X7 V1.1, KIT_AURIX_TC297_TFT_BC-Step, TC29xTA/TX_B-Step
 * \keywords AURIX, CAN, Gateway, Interrupt, Loop-Back, MULTICAN, MULTICAN_GW_TX_FIFO_1, TX FIFO
 * \documents https://www.infineon.com/aurix-expert-training/Infineon-AURIX_MULTICAN_GW_TX_FIFO_1_KIT_TC297_TFT-TR-v01_00_02-EN.pdf
 * \documents https://www.infineon.com/aurix-expert-training/TC29B_iLLD_UM_1_0_1_12_0.chm
 * \lastUpdated 2021-06-29
 *********************************************************************************************************************/
#include "Ifx_Types.h"
#include "IfxCpu.h"
#include "IfxScuWdt.h"
#include "MULTICAN_GW_TX_FIFO.h"

IfxCpu_syncEvent g_cpuSyncEvent = 0;

int core0_main(void)
{
    IfxCpu_enableInterrupts();

    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxScuWdt_disableCpuWatchdog(IfxScuWdt_getCpuWatchdogPassword());
    IfxScuWdt_disableSafetyWatchdog(IfxScuWdt_getSafetyWatchdogPassword());

    /* Wait for CPU sync event */
    IfxCpu_emitEvent(&g_cpuSyncEvent);
    IfxCpu_waitEvent(&g_cpuSyncEvent, 1);

    /* Application code: initialization of MULTICAN, LED, transmission and verification of the CAN messages */
    initMultican();
    initLed();
    transmitCanMessages();
    verifyCanMessages();

    /* If at this point the LED is not turned on, check "g_status" and "g_currentCanMessage" variables */
    while(1)
    {
    }
    return (1);
}
