/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define LED1                            &MODULE_P00,5
#define LED2                            &MODULE_P00,6

/* Setting one of these macros to 1 will provide a wrong expected CRC value to the FCE driver,
 * then the error interrupt will be triggered
 */
#define CRC32_KERNEL0_WRONG_CHECK_VAL   0
#define CRC32_KERNEL1_WRONG_CHECK_VAL   0
#define CRC16_WRONG_CHECK_VAL           0
#define CRC8_WRONG_CHECK_VAL            0

/* Setting macros for the expected results of the calculation */
#define CRC32_EXPECTED_RESULT           0xD95DEF75
#define CRC16_EXPECTED_RESULT           0xDA6F
#define CRC8_EXPECTED_RESULT            0x61

/* Setting macro for error injection */
#define CRC_ERROR_INJECTION             0xAC

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
fceCrc g_fceCrc; /* Structure to store information */

/* Data over which CRC should be calculated */
uint32 g_checkData[DATA_LENGTH_WORDS] = {
    0xbe9957bb,
    0x1c706c1e,
    0x14c3db3f,
    0x7fb17a93,
    0xb0d9d5a7,
    0x768093e0,
    0x88b206a0,
    0xc51299e4,
    0xe8a97d48,
    0x89367f27,
    0x70095984,
    0xec030f75,
    0xdc22f8d4,
    0xd951407b,
    0x34ae18c6,
    0x4d47ba7d,
    0x0e2e4622,
    0x4a2e90d3,
    0xdaec3752,
    0xcd3ed11c,
    0x36b416b7,
    0x8ea28658,
    0xdd37eee3,
    0x23928b62,
    0x84eb4b22,
};

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
IFX_INTERRUPT(fce_ISR, 0, INT_PRIO_FCE);

/* FCE ISR check length & CRC mismatch error, Bus & Configuration Error */
void fce_ISR(void)
{
    Ifx_FCE_STS interruptStatus;

    /* Get FCE CRC32 Kernel 0 interrupt status */
    interruptStatus = IfxFce_Crc_getInterruptStatus(&g_fceCrc.fceCrc32_0);

    /* Check if error flags are set */
    if(interruptStatus.U & FCE_CRC_ERROR_MASK != 0x0U)
    {
        /* Switch On LED1 and LED2 */
        IfxPort_setPinLow(LED1);
        IfxPort_setPinLow(LED2);
        /* Clear Error flags */
        IfxFce_Crc_clearErrorFlags(&g_fceCrc.fceCrc32_0);
    }

    /* Get FCE CRC32 Kernel 1 interrupt status */
    interruptStatus = IfxFce_Crc_getInterruptStatus(&g_fceCrc.fceCrc32_1);

    /* Check if error flags are set */
    if(interruptStatus.U & FCE_CRC_ERROR_MASK != 0x0U)
    {
        /* Switch On LED1 and LED2 */
        IfxPort_setPinLow(LED1);
        IfxPort_setPinLow(LED2);
        /* Clear Error flags */
        IfxFce_Crc_clearErrorFlags(&g_fceCrc.fceCrc32_1);
    }

    /* Get FCE CRC16 interrupt status */
    interruptStatus = IfxFce_Crc_getInterruptStatus(&g_fceCrc.fceCrc16);

    /* Check if error flags are set */
    if(interruptStatus.U & FCE_CRC_ERROR_MASK != 0x0U)
    {
        /* Switch Off LED1, Switch On LED2 */
        IfxPort_setPinHigh(LED1);
        IfxPort_setPinLow(LED2);
        /* Clear Error flags */
        IfxFce_Crc_clearErrorFlags(&g_fceCrc.fceCrc16);
    }

    /* Get FCE CRC interrupt status */
    interruptStatus = IfxFce_Crc_getInterruptStatus(&g_fceCrc.fceCrc8);

    /* Check if error flags are set */
    if(interruptStatus.U & FCE_CRC_ERROR_MASK != 0x0U)
    {
        /* Switch On LED1, Switch Off LED2 */
        IfxPort_setPinLow(LED1);
        IfxPort_setPinHigh(LED2);
        /* Clear Error flags */
        IfxFce_Crc_clearErrorFlags(&g_fceCrc.fceCrc8);
    }
}

/* Initialization of FCE
 * This function is called from main during initialization phase
 */
void init_FCE_CRC(void)
{
    /* Disable interrupts */
    boolean interruptState = IfxCpu_disableInterrupts();

    /* Create FCE module configuration */
    IfxFce_Crc_Config fceConfig;
    IfxFce_Crc_initModuleConfig(&fceConfig, &MODULE_FCE0);

    /* ISR priorities and interrupt target */
    fceConfig.isrPriority = INT_PRIO_FCE;
    fceConfig.isrTypeOfService = (IfxSrc_Tos)IfxCpu_getCoreIndex();

    /* Initialize module */
    IfxFce_Crc_initModule(&g_fceCrc.fce, &fceConfig);

    /* Initialize CRC kernels with default configuration */
    IfxFce_Crc_CrcConfig crcConfig;
    IfxFce_Crc_initCrcConfig(&crcConfig, &g_fceCrc.fce);

    /* For All CRC calculations */
    /* Enable Interrupt in case of CRC Mismatch */
    crcConfig.enabledInterrupts.crcMismatch = TRUE;

    /* Initialize FCE CRC32 Kernel 0 */
    crcConfig.crc32Kernel = IfxFce_Crc32Kernel_0;
    IfxFce_Crc_initCrc(&g_fceCrc.fceCrc32_0, &crcConfig);

    /* Initialize FCE CRC32 Kernel 1 */
    crcConfig.crc32Kernel = IfxFce_Crc32Kernel_1;
    IfxFce_Crc_initCrc(&g_fceCrc.fceCrc32_1, &crcConfig);

    /* Initialize FCE CRC16 */
    crcConfig.crcMode     = IfxFce_CrcMode_16;
    IfxFce_Crc_initCrc(&g_fceCrc.fceCrc16, &crcConfig);

    /* Initialize FCE CRC8 */
    crcConfig.crcMode = IfxFce_CrcMode_8;
    IfxFce_Crc_initCrc(&g_fceCrc.fceCrc8, &crcConfig);

    /* Setup the port pins connected to LEDs to general output mode push-pull. */
    IfxPort_setPinMode(LED1, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(LED2, IfxPort_Mode_outputPushPullGeneral);
    /* Turn OFF LEDs */
    IfxPort_setPinHigh(LED1);
    IfxPort_setPinHigh(LED2);

    /* Enable interrupts again */
    IfxCpu_restoreInterrupts(interruptState);
}


/* Start the calculation
 * This function is called from main, background loop
 */
void run_FCE_CRC(void)
{
    /* Set CRC32 expected value */
#if CRC32_KERNEL0_WRONG_CHECK_VAL == 0
    g_fceCrc.fceCrc32_0.expectedCrc = CRC32_EXPECTED_RESULT;
#else
    g_fceCrc.fceCrc32_0.expectedCrc = CRC_ERROR_INJECTION;
#endif
    /* CRC32 calculation with FCE Kernel 0 */
    g_fceCrc.crc32_kernel0_result = IfxFce_Crc_calculateCrc32(&g_fceCrc.fceCrc32_0, g_checkData, DATA_LENGTH_WORDS, 0x00000000);

    /* Set CRC32 expected value */
#if CRC32_KERNEL1_WRONG_CHECK_VAL == 0
    g_fceCrc.fceCrc32_1.expectedCrc = CRC32_EXPECTED_RESULT;
#else
    g_fceCrc.fceCrc32_1.expectedCrc = CRC_ERROR_INJECTION;
#endif
    /* CRC32 calculation with FCE Kernel 1 */
    g_fceCrc.crc32_kernel1_result = IfxFce_Crc_calculateCrc32(&g_fceCrc.fceCrc32_1, g_checkData, DATA_LENGTH_WORDS, 0x00000000);

    /* Set CRC16 expected value */
#if CRC16_WRONG_CHECK_VAL == 0
    g_fceCrc.fceCrc16.expectedCrc = CRC16_EXPECTED_RESULT;
#else
    g_fceCrc.fceCrc16.expectedCrc = CRC_ERROR_INJECTION;
#endif
    /* CRC16 calculation with FCE
     * DATA_LENGTH_WORDS is multiplied by 2 because the crcDataLength parameter must be provided as the number of 16-bit blocks */
    g_fceCrc.crc16_result = IfxFce_Crc_calculateCrc16(&g_fceCrc.fceCrc16, (uint16 *)g_checkData, (DATA_LENGTH_WORDS * 2), 0x00000000);

    /* Set CRC8 expected value */
#if CRC8_WRONG_CHECK_VAL == 0
    g_fceCrc.fceCrc8.expectedCrc = CRC8_EXPECTED_RESULT;
#else
    g_fceCrc.fceCrc8.expectedCrc = CRC_ERROR_INJECTION;
#endif
    /* CRC8 calculation with FCE
     * DATA_LENGTH_WORDS is multiplied by 4 because the crcDataLength parameter must be provided as the number of 8-bit blocks */
    g_fceCrc.crc8_result = IfxFce_Crc_calculateCrc8(&g_fceCrc.fceCrc8, (uint8 *)g_checkData, (DATA_LENGTH_WORDS * 4), 0x00000000);
}
